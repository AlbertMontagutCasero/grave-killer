namespace GraveKiller
{
    public class MapSize
    {
        public float width;
        public float height;
        public float startX;
        public float startY;

        public MapSize(float width, float height, float startX, float startY)
        {
            this.width = width;
            this.height = height;
            this.startX = startX;
            this.startY = startY;
        }
    }
}
